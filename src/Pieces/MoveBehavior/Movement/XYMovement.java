package Pieces.MoveBehavior.Movement;

public class XYMovement {
    private int dx;
    private int dy;

    public void setDx(int dx) {
        this.dx = dx;
    }

    public void setDy(int dy) {
        this.dy = dy;
    }

    public int getDy() {
        return dy;
    }

    public int getDx() {
        return dx;
    }
}
